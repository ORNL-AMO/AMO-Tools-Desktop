function setStateLines() {
    const stateLines = {
        type: 'scattergeo',
        mode: 'lines',
        lat: [],
        lon: [],
        line: {
            color: 'gray',
            width: 1
        },
        showlegend: false
    };
    GEO_DATA_STATE_LINES_RAW.features.forEach(feature => {
      if (feature.geometry.type === 'MultiLineString') {
        feature.geometry.coordinates.forEach(line => {
          line.forEach(coord => {
            this.stateLines.lat.push(coord[1]);
            this.stateLines.lon.push(coord[0]);
          });
          this.stateLines.lat.push(null); // Add null to separate line segments
          this.stateLines.lon.push(null);
        });
      } else if (feature.geometry.type === 'LineString') {
        feature.geometry.coordinates.forEach(coord => {
          this.stateLines.lat.push(coord[1]);
          this.stateLines.lon.push(coord[0]);
        });
        this.stateLines.lat.push(null); // Add null to separate line segments
        this.stateLines.lon.push(null);
      }
    });
    console.log(this.stateLines);
  }

